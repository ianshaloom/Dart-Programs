void main() {
  for (int i = 1; i <= 100; i++) {
    print(i);
  }
  
  /* Note that the control moves from initialization
  then checks the condition, if the condition is 
  the block is executed then the updation/increment is done, 
  We dont go back to initialization but we now start from the
  condition then block then updation, the process continues 
  until the condition is false */
}
