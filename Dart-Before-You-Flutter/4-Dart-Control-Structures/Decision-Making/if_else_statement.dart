/* If the result of the condition is true, 
then the body of the if-condition is executed.
Otherwise, the body of the else-condition is executed. */

void main() {
  int age = 12;

  if (age >= 18) {
    print("You are voter."); //print if condition true
  } else {
    print("You are not voter."); // print condition if false
  }
}
